describe('Admin - CRUD Subjects', () => {
  it('Login', () => {
    cy.visit('/')
    cy.contains('.v-toolbar__title', 'Login')
    cy.get('input[name="email"]')
      .clear()
      .type('admin@gmail.com')
    cy.get('input[name="password"]')
      .clear()
      .type('admin123')
    cy.get('.v-input__icon--append').click()
    cy.get('.v-input__icon--append').click()
    cy.get('button[type="submit"]').click()
    cy.url().should('include', 'admin')
  })
  it('Navigate to Subjects', () => {
    cy.get('.aio-menu-datasets').click()
    cy.get('.aio-menu-subjects').click()
    cy.url().should('include', 'subjects')
  })
  it('Create Subject', () => {
    cy.get('.aio-create').click()
    cy.get('input[name="identifier"]')
      .clear()
      .type('1234567')
    cy.get('input[name="name"]')
      .clear()
      .type('A Subject 1')
    cy.get('.aio-edit-save').click()
  })
  it('Edit Subject', () => {
    cy.get('.aio-edit-a-subject-1').click()
    cy.get('input[name="identifier"]')
      .clear()
      .type('7654321')
    cy.get('input[name="name"]')
      .clear()
      .type('A SUBJECT 2')
    cy.get('.aio-edit-save').click()
  })
  it('Delete Subject', () => {
    cy.get('.aio-delete-a-subject-2').click()
    cy.get('.aio-remove').click()
  })
})
